  <p>Ketika hewan peliharaan mendapatkan kasih sayang yang tulus dari tuannya, tidak jarang hewan tersebut akan menjadi hewan yang begitu setia. Hewan itu bahkan bisa menjadi teman sekaligus sahabat yang akan mengingat sang tuan hingga kapan pun. Jika selama ini kita mengenal anjing sebagai hewan yang begitu setia ke tuannya, kucing ternyata juga tak kalah setia dan mengingat sang tuan dengan sangat baik. Dilansir dari laman today.com, seekor kucing yang telah berpisah dengan tuannya selama 14 tahun akhirnya bisa bertemu kembali dengan sang tuan, seorang pria bernama Perry Martin asal Florida.</p>
